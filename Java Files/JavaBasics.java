public class JavaBasics {
    public static void main(String args[]) {
        System.out.print("Hello, Java"); // Print Hello Java
        System.out.print("Hello, Java"); // Print Hello Java
        System.out.print("Hello, Java"); // Print Hello Java
        
        System.out.println("Hello, Java"); // Print Hello Java in new line due to println instead of print command
        System.out.print("Hello, Java\n"); // Can also use \n for new line

        // Pattern

        System.out.println("****"); 
        System.out.println("***"); 
        System.out.println("**"); 
        System.out.println("*"); 

    }
}